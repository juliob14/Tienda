/**
 *Hacer un pseudocodigo que imprima los numeros del 100 al 0, en orden
decreciente.

 * @author juliobitar
 */
public class prob2 {
    public static void main(String[] args) {
        int c =101;
        while (c>0){
             c--;
             System.out.println("Imprime:"+c);
        }
           
        
    }
    
}
